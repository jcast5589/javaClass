public class EjemploVariables {
    public static void main(String[] args) {
        int entero = 10; // Declarar un entero
        double decimal = 5.5; // Declarar un número decimal
        boolean esVerdadero = true; // Declarar un booleano
        char caracter = 'A'; // Declarar un carácter
        String texto = "Hola, Java"; // Declarar una cadena de texto
        
        // Operaciones con variables
        int suma = entero + 5; // Sumar a la variable entero
        double multiplicacion = decimal * 2; // Multiplicar el decimal por 2
        
        // Concatenación de cadenas de texto
        String mensaje = texto + ", " + "Bienvenido!"; // Concatenar cadenas
        
        // Imprimir resultados
        System.out.println("Suma: " + suma);
        System.out.println("Multiplicación: " + multiplicacion);
        System.out.println(mensaje);
    }
}
